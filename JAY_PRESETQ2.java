package com.company;
import java.util.Scanner;
public class JAY_PRESETQ2 {
    public static void main(String[] args) {
        int Day;
        Scanner sc=new Scanner(System.in);
        Day=sc.nextInt();
        switch(Day){
            case 1:
            System.out.println(" Monday ");
            break;
            case 2:
            System.out.println(" Tuesday ");
            break;
            case 3:
            System.out.println(" Wednesday ");
            break;
        }
    }
}
